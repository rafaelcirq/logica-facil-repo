<p>O aplicativo VAR propõe uma dinâmica para a aprendizagem do conceito de Variável.

<p>Em programação, uma variável representa o espaço reservado na memória do computador para armazenar um valor que será manipulado pelo programa.

<p>Aqui, iremos visualizar a memória do computador como um conjunto de envelopes.

<p>Para cada envelope, será possível definir um nome e atribuir a ele um conteúdo. Desta forma, investigaremos duas definições importantes para variáveis: 
<p>* Nome da Variável
<p>* Conteúdo armazenado na variável

<p>Você deverá seguir algumas regras para definir nomes para suas variáveis (envelopes):
<p><p>* Devem começar por um caracter alfabético;
<p><p>* Podem ser seguidos por mais caracteres alfabéticos ou numéricos; 
<p><p>* Não devem ser usados caracteres especiais como +,-,/,*,(,[,{,%,$,# 
<p><p>* Não se usa acentuação;
<p><p>* Não pode conter espaços no nome;
<p><p>* Pode-se utilizar  letras maiúsculas ou minúsculas, porém sua variável deve ser usada exatamente como foi criada.
<p><p>Para adicionar um conteúdo a uma variável, obedeça aos padrões a seguir:
<p><p>* Selecione uma variável que você já nomeou
<p><p>* Defina um valor numérico de no máximo quatro dígitos. Ex: 42.05 ou -123

<p>Após criar suas variáveis, será possível escrever comandos na aba "Compilador" para manipulá-las.
