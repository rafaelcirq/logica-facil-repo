                Você deverá seguir algumas regras ao definir nomes para suas variáveis (envelopes):<br><br>
                1 - Deve começar com um caracter alfabético (nada de números no início)<br>
                2 - Pode utilizar caracteres alfabéticos ou numéricos a seguir<br>
                3 - Não deve usar caracteres especiais (+, -, /, *, (, [, {, %, $, #)<br>
                4 - Não deve usar acentuação<br>
                5 - Não deve usar espaço<br>
                6 - Pode usar letras maiúsculas ou minúsculas (mas deve utilizar exatamente como nomeou)

