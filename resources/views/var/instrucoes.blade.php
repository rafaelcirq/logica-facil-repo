                ao digitar "escreva(numero)" e clicar em "Compilar" o valor "12" será exibido no monitor.<br>
                Com ele também é possível fazer operações matematicas simples de dois numeros. Por exemplo, o comando
                "escreva(1+1)" exibe o resultado "2" no monitor.<br>
                Você também pode misturar os dois, escrevendo "escreva(numero+2)". Neste caso, se o envelope "numero"
                ainda tiver o valor "12", será exibido o número "14" no monitor.<br><br>
                2 - "numero = 2": se você colocar o nome de uma variável seguido por " = " e um valor, aquela variável
                recebe o valor digitado.<br>
                Com isso, você também consegue atribuir a ele um valor de outra variável, por
                exemplo: "numero_1 = numero_2", ou receber o resultado de uma expressão matemática, por exemplo:
                "numero_2 = numero_1*2"<br><br>
